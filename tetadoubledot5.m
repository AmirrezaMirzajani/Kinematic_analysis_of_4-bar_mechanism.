function Tetadoubledot5=tetadoubledot5(r5,a,r4,teta1,teta2,r1,r2,tetadot2,tetadoubledot2)
     
         Tetadoubledot5=(r5*tetadot5(r4,r5,teta1,teta2,r1,r2,tetadot2,a)^2 ...
         *sin(teta5(a,r4,teta1,teta2,r1,r2,r5))...
         -r4*tetadoubledot3(r2,teta2,tetadoubledot2,r1,teta1,tetadot2)...
         *cos(teta3(teta1,teta2,r1,r2))...
         +r4*(tetadot3(tetadot2,r2,teta2,teta1,r1))^2 ...
         *sin(teta3(teta1,teta2,r1,r2)))...
         /(r5*cos(teta5(a,r4,teta1,teta2,r1,r2,r5)));
     
end
